# dutree  : Tree with size info
# ytop  : `htop` alternative
# bottom  : `htop` alternative
# procs  : Alternative to `ps`. This enables a user to see the processes in more detail
# skim  : Fuzzy finder and an interactive alternative to `grep`
# bat  : clone of `cat` command but with syntax highlighting
# exa  : Modern alternative to `ls` command
# fd  : Alternative to find (fuzzy)
# starship  : A very fast and highly customizable prompt for any shell
# ripgrep  : Alternative to grep
# tokei  : Lists the languages, sloc in each directory
# tealdeer  : Modern manpages with more info
# bandwhich  : Bandwidth display
# grex  : Test cases to regex
# rmesg  : Kernel message logger
# zoxide  : Jump from any directory to any other
# delta  : Git-difference
# tp-note  : Clipboard to save-as-note
# nu  : New shell